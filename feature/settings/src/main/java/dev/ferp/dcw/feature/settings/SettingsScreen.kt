package dev.ferp.dcw.feature.settings

import androidx.compose.foundation.layout.Box
import androidx.compose.foundation.layout.padding
import androidx.compose.material3.ExperimentalMaterial3Api
import androidx.compose.material3.Scaffold
import androidx.compose.material3.Text
import androidx.compose.material3.TopAppBar
import androidx.compose.runtime.Composable
import androidx.compose.ui.Modifier
import androidx.compose.ui.res.stringResource
import androidx.compose.ui.viewinterop.AndroidViewBinding
import dev.ferp.dcw.feature.settings.databinding.FragmentContainerBinding

@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun SettingsScreen(
    versionName: String,
    modifier: Modifier = Modifier
) {
    Scaffold(
        modifier = modifier,
        topBar = {
            TopAppBar(
                title = {
                    Text(text = stringResource(R.string.settings))
                }
            )
        }
    ) { paddingValues ->
        Box(Modifier.padding(paddingValues)) {
            AndroidViewBinding(FragmentContainerBinding::inflate) {
                val settingsFragment = fragmentContainerView.getFragment<SettingsFragment>()
                settingsFragment.setVersionName(versionName)
            }
        }
    }
}